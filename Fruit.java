package InheritancePolymorphism;

public class Fruit {
    public void WashFruit()
    {
        System.out.println("Fruit-------Wash");
    }
    public void CheckFruit()
    {
        System.out.println("Fruit-------Check");
    }
    public void EatFruit()
    {
        System.out.println("Fruit-------Eat");
    }
    public void GainVitamin()
    {
        System.out.println("Fruit-------Gain Vitamin C");
    }
    public void TasteFruit()
    {
        System.out.println("Fruit-------Taste is Sour");
    }

}

